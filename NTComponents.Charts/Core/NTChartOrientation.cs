namespace NTComponents.Charts.Core;

/// <summary>
///     Specifies the orientation of a cartesian chart.
/// </summary>
public enum NTChartOrientation
{
    /// <summary>
    ///     The independent variable (X) is on the horizontal axis and the dependent variable (Y) is on the vertical axis.
    /// </summary>
    Vertical,

    /// <summary>
    ///     The independent variable (X) is on the vertical axis and the dependent variable (Y) is on the horizontal axis.
    /// </summary>
    Horizontal
}
